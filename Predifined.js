// Predefined functions
// eval()   function evaluates JavaScript code represented as a string and 
// returns its completion value. The source is parsed as a script.

// example

console.log(eval('2+2'));
// expected output is 4

// Syntax

// eval('string')

// Parameters is a String 
/*
    A string representing a JavaScript expression, statement, or 
    sequence of statements. The expression can include variables
    and properties of existing objects.
*/

//Return Value
/*
    The completion value of evaluating the given code. If the completion
    value is empty, undefined is returned.
*/
